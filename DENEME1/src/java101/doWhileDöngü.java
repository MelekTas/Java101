package java101;

public class doWhileDöngü {

	public static void main(String[] args) {
		
		int year=2021;
		do {
			System.out.println(year);
			year++;
		}
	  
		while(year<2024);
				
		// while true olsa da olmasa da do bir kere çalışır.
		// sonrasında while ın sağlandığı her durumda do çalışır.
		
		
	}

}
